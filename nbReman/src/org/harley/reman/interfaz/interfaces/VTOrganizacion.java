    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here: MODIFICAR
        
        TreePath selPath = trOrganizacion.getSelectionPath();
        String parent = selPath.getParentPath().getLastPathComponent().toString();
        
        if ( parent.equals("Organización")){
            VOrganizacion modOrganizacion = new VOrganizacion();
            modOrganizacion.setVisible(true);
        }
        else if( parent.equals("Autor")){
            VActor modActor = new VActor();
            modActor.setVisible(true);
        }
        else{
            VFuente modFuente = new VFuente();
            modFuente.setVisible(true);
        }
            
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here: ELIMINAR
        TreePath selPath = trOrganizacion.getSelectionPath();
        String parent = selPath.getParentPath().getLastPathComponent().toString();
        
        if ( parent.equals("Organización")){
            int resp = JOptionPane.showConfirmDialog(null, "Eliminar Organización", "Alerta!", JOptionPane.YES_NO_OPTION);
            System.out.println("Eliminare una fuente");
        }
        else if( parent.equals("Autor")){
            int resp = JOptionPane.showConfirmDialog(null, "Eliminar Autor", "Alerta!", JOptionPane.YES_NO_OPTION);
        }
        else{
            int resp = JOptionPane.showConfirmDialog(null, "Eliminar Fuente", "Alerta!", JOptionPane.YES_NO_OPTION);
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

