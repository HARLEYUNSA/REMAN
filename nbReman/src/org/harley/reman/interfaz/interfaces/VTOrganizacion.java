    MouseListener ml = new MouseAdapter(){
        public void mouseClicked(MouseEvent e) {
         // Se obtiene el path para esa fila. Con el path podemos obtener
         // los nodos.
            if (e.getButton()==3){
                TreePath selPath = trOrganizacion.getPathForLocation(e.getX(), e.getY());
                trOrganizacion.setSelectionPath(selPath);
                String node = selPath.getLastPathComponent().toString();
                if(selPath !=null){
                    if(node.equals("Documentos de la Organización")|| node.equals("Autor") || node.equals("Fuente")){
                    }
                    else if(node.equals("Organización")){
                       menu1.show(e.getComponent(), e.getX(),e.getY()+10);
                    }
                    else if(node.equals("Actores")){
                       menu3.show(e.getComponent(), e.getX(),e.getY()+10);
                    }
                    else{
                       menu2.show(e.getComponent(), e.getX(),e.getY()+10);
                    }
                }
            }
        }
    };
