    private void JListPropiosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JListPropiosMouseClicked
        int selection = JListPropios.getSelectedIndex();
        ArrayList<String> listMod = new ArrayList<>();
        String temp;
        if (selection!=-1) {
            listMod.addAll(ToolsInterface.jList2Array(JListSeleccionados));
            temp = String.valueOf(JListPropios.getSelectedValue());
            if(!listMod.contains(temp)){
                listMod.add(String.valueOf(JListPropios.getSelectedValue()));
                JListSeleccionados.setModel(ToolsInterface.modelList(listMod));
            }
        }

    }//GEN-LAST:event_JListPropiosMouseClicked

    private void JListSeleccionadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JListSeleccionadosMouseClicked
        int selection = JListSeleccionados.getSelectedIndex();
        ArrayList<String> listMod = new ArrayList<>();
        if (selection!=-1) {
            listMod.addAll(ToolsInterface.jList2Array(JListSeleccionados));
            listMod.remove(selection);
            JListSeleccionados.setModel(ToolsInterface.modelList(listMod));
        }

    }//GEN-LAST:event_JListSeleccionadosMouseClicked


